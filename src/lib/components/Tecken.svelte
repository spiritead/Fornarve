<section>
    <div class="titel">
        <span></span><h2>Tecken</h2><span></span>
    </div>
    <p>Vilka tecken behövs? <br>
        En målsättning bör vara att varje ljud i svenskan ska ha ett enda tecken, alltså inte skrivas med kombinationer av tecken.</p>
    <div class="container">
        <div class="card">
            <h2>”Sje”-ljudet</h2>
            <div>
                <p>är ju ett exempel på ett ljud med en mängd olika stavningar. Mitt förslag är, att ersätta alla olika varianter av stavningar av sje-ljudet med tecknet x. X används ju i dag som ett tecken för två ljud, nämligen k+s, vilket ur ”ljudenlighetssynpunkt” inte är särskilt lyckat. Förhållandet kan alltså egentligen betecknas som det motsatta jämfört med sje-ljudet. Ljudkombinationen k+s bör följaktligen skrivas ks, som t ex i norskan. I så fall skulle tecknet x kunna ”frigöras” för nyttigare ändamål, nämligen som samlande tecken för sje-ljudet. Faktum är att detta tecken i flera språk betecknar just en sorts sje-ljud, liksom också i fonetisk skrift.</p>
            </div>
        </div>
        <div class="card">
            <h2>e & å ljuden</h2>
            <div>
                <p>I nutida svenska sammanfaller i de allra flesta svenskars talspråk kort e-ljud och kort ä-ljud. Detta korta ”gemensamma” vokalljud kan höras t ex i ordparen vänta-ventil och i (med en något ”mörkare” variant före r) i ordparen berg-färg. Jag föreslår att detta ljud i den nya stavningen skrivs som e, vilket medför att tecknet ä endast används för det långa ä-ljudet, och då givetvis dubbeltecknat.</p>
                <p>Stavningen av ”å”-ljudet är ett annat problem i den nuvarande stavningen. Mitt förslag är att å-ljudet alltid stavas med å, (”som” blir såm, ”sova” blir sååva), och att tecknet o reserveras för ”o”-ljud (t ex tom, skoog).
                </p>
            </div>
        </div>
        <div class="card">
            <h2>ng-ljudet</h2>
            <div>
                <p>Ett ljud, som även fortsättningsvis kan bli svårt att beteckna med endast ett tecken är ng-ljudet. Här finns ingen bra kandidat bland de återstående tecken, (q och w), som blir ”onödiga” i den nya stavningen. Däremot finns det faktiskt ett tecken för detta ljud, som återfinns i bl a samisk teckenuppsättning och även i fonetisk skrift, vilken skulle kunna användas, nämligen tecknet ”ŋ” (i versal form ”Ŋ”).</p>
            </div>
        </div>
        <div class="card">
            <h2>'Tje'-ljudet</h2>
            <div>
                <p>Ett annat tecken som ”tappar” sin ursprungliga funktion är c. Teckenkombinationen ck försvinner ju om alla konsonanter enkeltecknas, och i alla andra sammanhang kommer c att ersättas av tecknet s eller k. Däremot skulle c med fördel kunna användas som tecken för ”tje”-ljudet. Även här finns kopplingar till andra språk, t ex italienska, där c före mjuk vokal betecknar tje-ljud.</p>
            </div>
        </div>
    </div>
